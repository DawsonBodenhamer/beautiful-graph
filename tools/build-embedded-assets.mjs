import esbuild from "esbuild";
import {mkdirSync,readFileSync,writeFileSync} from "node:fs";
import {dirname,resolve} from "node:path";

const root=resolve(import.meta.dirname,"..");
const wasm=readFileSync(resolve(root,"graph-sim.wasm"));
if(wasm.length<8||wasm.subarray(0,4).toString("hex")!=="0061736d")throw new Error("graph-sim.wasm is not a valid Wasm artifact.");

const result=await esbuild.build({
  entryPoints:[resolve(root,"src/graph-worker.ts")],
  bundle:true,
  platform:"browser",
  format:"iife",
  target:"es2021",
  treeShaking:true,
  minify:true,
  write:false,
  banner:{js:"/* Embedded Beautiful Graph physics worker. */"},
});
const worker=result.outputFiles?.[0]?.text;
if(!worker)throw new Error("The embedded physics worker bundle was not generated.");

const destination=resolve(root,".generated/embedded-assets.ts");
mkdirSync(dirname(destination),{recursive:true});
writeFileSync(destination,
  `/* Generated by tools/build-embedded-assets.mjs. Do not edit. */\n`+
  `export const EMBEDDED_WORKER_SOURCE=${JSON.stringify(worker)};\n`+
  `export const EMBEDDED_WASM_BASE64=${JSON.stringify(wasm.toString("base64"))};\n`,
  "utf8",
);
console.log(`Embedded ${Buffer.byteLength(worker)} worker bytes and ${wasm.length} Wasm bytes.`);
