import {readFileSync,statSync} from "node:fs";
import {resolve} from "node:path";

const root=resolve(import.meta.dirname,"..");
const readJson=path=>JSON.parse(readFileSync(resolve(root,path),"utf8"));
const contract=readJson("config/v2_artifacts.json"),manifest=readJson("manifest.json"),pkg=readJson("package.json");
if(contract.releaseVersion!==manifest.version||manifest.version!==pkg.version)throw new Error(`Release version mismatch: contract=${contract.releaseVersion}, manifest=${manifest.version}, package=${pkg.version}`);
if(manifest.isDesktopOnly!==true)throw new Error("Beautiful Graph 2.0 must be desktop-only.");
const mainSource=readFileSync(resolve(root,"main.js"),"utf8");
if(/\beval\s*\(|\bnew\s+Function\s*\(/u.test(mainSource))throw new Error("main.js contains dynamic JavaScript execution.");
for(const artifact of contract.artifacts){
  if(!artifact.required)continue;
  const path=resolve(root,artifact.path),stat=statSync(path,{throwIfNoEntry:false});
  if(!stat?.isFile()||stat.size===0)throw new Error(`Required production artifact is missing or empty: ${artifact.path}`);
}
const main=readFileSync(resolve(root,"main.js"),"utf8");if(!main.startsWith("/* Generated by esbuild."))throw new Error("Unexpected or stale bundle: main.js");
const wasm=readFileSync(resolve(root,"graph-sim.wasm"));if(wasm.length<8||wasm.subarray(0,4).toString("hex")!=="0061736d")throw new Error("graph-sim.wasm is not a valid Wasm artifact.");
if(main.length<=wasm.length)throw new Error("main.js does not contain the embedded runtime payload.");
const workerFactory=readFileSync(resolve(root,"src/physics-worker.ts"),"utf8"),view=readFileSync(resolve(root,"src/graph-view.ts"),"utf8");
if(!workerFactory.includes("EMBEDDED_WORKER_SOURCE")||!workerFactory.includes("EMBEDDED_WASM_BASE64"))throw new Error("Physics worker does not consume the embedded runtime payload.");
if(view.includes("FileSystemAdapter")||view.includes("graph-worker.js")||view.includes("graph-sim.wasm"))throw new Error("Production graph view still depends on separately installed runtime assets.");
console.log(`Verified ${contract.artifacts.length} Beautiful Graph ${manifest.version} release artifacts with embedded worker and Wasm.`);
